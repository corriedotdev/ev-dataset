CREATE TABLE Dynamic (
    NAME VARCHAR(255) NOT NULL,
    ID INT NOT NULL,
    SITEID INT NOT NULL,
    time DATETIME NOT NULL,
    PRIMARY KEY (NAME)
);

CREATE TABLE DynamicConnectorGroups (
    NAME VARCHAR(255) NOT NULL,
    CONNECTORID INT NOT NULL,
    CONNECTORSTATUS VARCHAR(255) NOT NULL,
    PRIMARY KEY (NAME,CONNECTORID)
);

